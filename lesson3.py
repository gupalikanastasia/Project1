# –°—Ç–≤–æ—Ä—é—î–º–æ –ø–æ—Ä–æ–∂–Ω—ñ–π —Å–ª–æ–≤–Ω–∏–∫ –¥–ª—è –∑–±–µ—Ä—ñ–≥–∞–Ω–Ω—è —ñ–º–µ–Ω —Ç–∞ –æ—Ü—ñ–Ω–æ–∫ —Å—Ç—É–¥–µ–Ω—Ç—ñ–≤
student_grades = {}

print("üéì –ü—Ä–æ–≥—Ä–∞–º–∞ –¥–ª—è –æ–±–ª—ñ–∫—É –æ—Ü—ñ–Ω–æ–∫ —Å—Ç—É–¥–µ–Ω—Ç—ñ–≤ üéì")
print("–í–≤–æ–¥—å—Ç–µ —ñ–º'—è —Å—Ç—É–¥–µ–Ω—Ç–∞ —Ç–∞ –π–æ–≥–æ –æ—Ü—ñ–Ω–∫—É. –î–ª—è –∑–∞–≤–µ—Ä—à–µ–Ω–Ω—è –≤–≤–µ–¥—ñ—Ç—å 'stop'.")

# –ó–∞–ø—É—Å–∫–∞—î–º–æ –Ω–µ—Å–∫—ñ–Ω—á–µ–Ω–Ω–∏–π —Ü–∏–∫–ª –¥–ª—è –≤–≤–µ–¥–µ–Ω–Ω—è –¥–∞–Ω–∏—Ö
while True:
    # –ó–∞–ø–∏—Ç—É—î–º–æ —ñ–º'—è —Å—Ç—É–¥–µ–Ω—Ç–∞
    student_name = input("\n–í–≤–µ–¥—ñ—Ç—å —ñ–º'—è —Å—Ç—É–¥–µ–Ω—Ç–∞: ")

    # –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ, —á–∏ –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á —Ö–æ—á–µ –∑–∞–≤–µ—Ä—à–∏—Ç–∏ –≤–≤–µ–¥–µ–Ω–Ω—è
    if student_name.lower() == 'stop':
        break  # –í–∏—Ö–æ–¥–∏–º–æ –∑ —Ü–∏–∫–ª—É

    # –ó–∞–ø–∏—Ç—É—î–º–æ –æ—Ü—ñ–Ω–∫—É –¥–ª—è –≤–≤–µ–¥–µ–Ω–æ–≥–æ —Å—Ç—É–¥–µ–Ω—Ç–∞
    try:
        grade_input = input(f"–í–≤–µ–¥—ñ—Ç—å –æ—Ü—ñ–Ω–∫—É –¥–ª—è {student_name} (–≤—ñ–¥ 1 –¥–æ 12): ")
        grade = int(grade_input)

        # –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞, —á–∏ –æ—Ü—ñ–Ω–∫–∞ –∑–Ω–∞—Ö–æ–¥–∏—Ç—å—Å—è –≤ –¥–æ–ø—É—Å—Ç–∏–º–æ–º—É –¥—ñ–∞–ø–∞–∑–æ–Ω—ñ
        if not 1 <= grade <= 12:
            print("‚ö†Ô∏è –ü–æ–º–∏–ª–∫–∞: –û—Ü—ñ–Ω–∫–∞ –º–∞—î –±—É—Ç–∏ –≤ –¥—ñ–∞–ø–∞–∑–æ–Ω—ñ –≤—ñ–¥ 1 –¥–æ 12. –°–ø—Ä–æ–±—É–π—Ç–µ —â–µ —Ä–∞–∑.")
            continue  # –ü–µ—Ä–µ—Ö–æ–¥–∏–º–æ –¥–æ –Ω–∞—Å—Ç—É–ø–Ω–æ—ó —ñ—Ç–µ—Ä–∞—Ü—ñ—ó —Ü–∏–∫–ª—É

        # –î–æ–¥–∞—î–º–æ —ñ–º'—è —Ç–∞ –æ—Ü—ñ–Ω–∫—É –¥–æ —Å–ª–æ–≤–Ω–∏–∫–∞
        student_grades[student_name] = grade
        print(f"‚úÖ –î–∞–Ω—ñ –¥–ª—è {student_name} —É—Å–ø—ñ—à–Ω–æ –¥–æ–¥–∞–Ω–æ.")

    except ValueError:
        # –û–±—Ä–æ–±–∫–∞ –ø–æ–º–∏–ª–∫–∏, —è–∫—â–æ –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á –≤–≤—ñ–≤ –Ω–µ —á–∏—Å–ª–æ
        print("‚ö†Ô∏è –ü–æ–º–∏–ª–∫–∞: –ë—É–¥—å –ª–∞—Å–∫–∞, –≤–≤–µ–¥—ñ—Ç—å —á–∏—Å–ª–æ–≤–µ –∑–Ω–∞—á–µ–Ω–Ω—è –¥–ª—è –æ—Ü—ñ–Ω–∫–∏.")

# --- –ê–Ω–∞–ª—ñ–∑ —Ç–∞ –≤–∏–≤–µ–¥–µ–Ω–Ω—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ñ–≤ ---

# –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ, —á–∏ –±—É–ª–∏ –≤–≤–µ–¥–µ–Ω—ñ –¥–∞–Ω—ñ
if not student_grades:
    print("\n–í–∏ –Ω–µ –≤–≤–µ–ª–∏ –∂–æ–¥–Ω–∏—Ö –¥–∞–Ω–∏—Ö. –ü—Ä–æ–≥—Ä–∞–º—É –∑–∞–≤–µ—Ä—à–µ–Ω–æ.")
else:
    print("\n" + "=" * 30)
    print("üìä –†–ï–ó–£–õ–¨–¢–ê–¢–ò –£–°–ü–Ü–®–ù–û–°–¢–Ü –ì–†–£–ü–ò üìä")
    print("=" * 30)

    # 1. –í–∏–≤–µ–¥–µ–Ω–Ω—è —Å–ø–∏—Å–∫—É –≤—Å—ñ—Ö —Å—Ç—É–¥–µ–Ω—Ç—ñ–≤ —Ç–∞ —ó—Ö –æ—Ü—ñ–Ω–æ–∫
    print("\n--- –ó–∞–≥–∞–ª—å–Ω–∏–π —Å–ø–∏—Å–æ–∫ —Å—Ç—É–¥–µ–Ω—Ç—ñ–≤ ---")
    for name, grade in student_grades.items():
        print(f"{name}: {grade}")

    # 2. –†–æ–∑—Ä–∞—Ö—É–Ω–æ–∫ —Å–µ—Ä–µ–¥–Ω—å–æ–≥–æ –±–∞–ª—É
    total_grades = sum(student_grades.values())
    number_of_students = len(student_grades)
    average_grade = total_grades / number_of_students
    print(f"\nüìà –°–µ—Ä–µ–¥–Ω—ñ–π –±–∞–ª –ø–æ –≥—Ä—É–ø—ñ: {average_grade:.2f}")

    # 3. –ü—ñ–¥—Ä–∞—Ö—É–Ω–æ–∫ –∫—ñ–ª—å–∫–æ—Å—Ç—ñ —Å—Ç—É–¥–µ–Ω—Ç—ñ–≤ –ø–æ –∫–∞—Ç–µ–≥–æ—Ä—ñ—è—Ö
    excellent_students = {name: grade for name, grade in student_grades.items() if 10 <= grade <= 12}
    good_students_count = len([grade for grade in student_grades.values() if 7 <= grade <= 9])
    lagging_students_count = len([grade for grade in student_grades.values() if 4 <= grade <= 6])
    failed_students_count = len([grade for grade in student_grades.values() if 1 <= grade <= 3])

    print("\n--- –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ –ø–æ –≥—Ä—É–ø–∞—Ö ---")
    # –í—ñ–¥–º—ñ–Ω–Ω–∏–∫–∏ (10-12)
    print(f"‚≠ê –í—ñ–¥–º—ñ–Ω–Ω–∏–∫–∏ (10-12 –±–∞–ª—ñ–≤): {len(excellent_students)}")
    if excellent_students:
        # –í–∏–≤–æ–¥–∏–º–æ —ñ–º–µ–Ω–∞ –≤—ñ–¥–º—ñ–Ω–Ω–∏–∫—ñ–≤, —è–∫—â–æ –≤–æ–Ω–∏ —î
        print(f"   –Ü–º–µ–Ω–∞: {', '.join(excellent_students.keys())}")

    # –•–æ—Ä–æ—à–∏—Å—Ç–∏ (7-9)
    print(f"üëç –•–æ—Ä–æ—à–∏—Å—Ç–∏ (7-9 –±–∞–ª—ñ–≤): {good_students_count}")

    # –í—ñ–¥—Å—Ç–∞—é—á—ñ (4-6)
    print(f"ü§î –í—ñ–¥—Å—Ç–∞—é—á—ñ (4-6 –±–∞–ª—ñ–≤): {lagging_students_count}")

    # –ù–µ –∑–¥–∞–ª–∏ (1-3)
    print(f"üò• –ù–µ –∞—Ç–µ—Å—Ç–æ–≤–∞–Ω—ñ (1-3 –±–∞–ª–∏): {failed_students_count}")

    print("\n" + "=" * 30)